# Numbers

for x in range(3):
    for y in range(1,4):
        print(x+y,end = ' ')
    print()

# alphabets

for x in range(3):
    for y in range(0,3):
        print(chr(x+y+97),end = ' ')
    print() 
    
# uppercase

for x in range(3):
    for y in range(0,3):
        print(chr(x+y+65),end = ' ')
    print()